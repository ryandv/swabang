#include <avr/io.h>

#define _PORT _SFR_IO_ADDR(PORTB)
#define _BIT  PORTB0

.section .text

; ws2812b_color(red, green, blue)
;
; arguments:
; r24 <- red  ;  r22 <- green  ;  r20 <- blue
;
; locals:
; r18 <- current bit index into one of red/green/blue

.global ws2812b_color
ws2812b_color:
           ldi r18, 8
   L_GXH:  sbi _PORT, _BIT
           subi r18, 1
           nop
           sbrs r22, 7
           rjmp L_G0L
           nop
           nop
           nop
           nop
           nop
           rjmp L_G1L
   L_G0L:  cbi _PORT, _BIT
           nop
           nop
           nop
           nop
           rjmp L_GEND
   L_G1L:  cbi _PORT, _BIT
  L_GEND:  cpi r18, 0
           breq L_RSTART
           lsl r22
           nop
           rjmp L_GXH
 L_RSTART: nop
           nop
           ldi r18, 8
   L_RXH:  sbi _PORT, _BIT
           subi r18, 1
           nop
           sbrs r24, 7
           rjmp L_R0L
           nop
           nop
           nop
           nop
           nop
           rjmp L_R1L
   L_R0L:  cbi _PORT, _BIT
           nop
           nop
           nop
           nop
           rjmp L_REND
   L_R1L:  cbi _PORT, _BIT
  L_REND:  cpi r18, 0
           breq L_BSTART
           lsl r24
           nop
           rjmp L_RXH
 L_BSTART: nop
           nop
           ldi r18, 8
   L_BXH:  sbi _PORT, _BIT
           subi r18, 1
           nop
           sbrs r20, 7
           rjmp L_B0L
           nop
           nop
           nop
           nop
           nop
           rjmp L_B1L
   L_B0L:  cbi _PORT, _BIT
           nop
           nop
           nop
           nop
           rjmp L_BEND
   L_B1L:  cbi _PORT, _BIT
  L_BEND:  cpi r18, 0
           breq L_EXIT
           lsl r20
           nop
           rjmp L_BXH
  L_EXIT:  ret

.global ws2812b_reset
ws2812b_reset:
